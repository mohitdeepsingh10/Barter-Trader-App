public class SearchMapActivity extends AppCompatActivity {
    private DatabaseReference productsRef;
    GoogleMap map;

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        EdgeToEdge.enable(this);
        setContentView(R.layout.activity_search_map);
        ViewCompat.setOnApplyWindowInsetsListener(findViewById(R.id.main), (v, insets) -> {
            Insets systemBars = insets.getInsets(WindowInsetsCompat.Type.systemBars());
            v.setPadding(systemBars.left, systemBars.top, systemBars.right, systemBars.bottom);
            return insets;
        });

        productsRef = FirebaseDatabase.getInstance().getReference("PRODUCT");

        Bundle mapSearchInfo = getIntent().getExtras();

        setupMap(mapSearchInfo);
        setupBackButton();
    }

    private void setupBackButton() {
        Button backButton = findViewById(R.id.backButton);
        backButton.setOnClickListener(new View.OnClickListener() {
            @Override
            public void onClick(View view) {
                finish();
            }
        });
    }

    private void setupMap(Bundle mapSearchInfo) {
        MapsFragment mapsFragment = MapsFragment.newInstance(mapSearchInfo);
        getSupportFragmentManager().beginTransaction().replace(R.id.fragmentContainerView, mapsFragment).commit();
    }
}